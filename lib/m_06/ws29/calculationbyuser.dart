
// taking value from user for subtraction

import 'dart:io';
void main(){

  print("Enter first value no1: ");
  int no1 = int.parse(stdin.readLineSync().toString());

  print("Enter second value no2: ");
  int no2 = int.parse(stdin.readLineSync().toString());

  int subtract = no1 - no2;

  // output
  stdout.write("The value of subtraction of $no1 and $no2 is $subtract");
}



